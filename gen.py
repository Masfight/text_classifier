import csv
import random

import os
os.makedirs('data', exist_ok=True)

# Шаблоны фраз для каждой эмоции
templates = {
    'радость': [
        "Я сегодня очень рад, что {}",
        "Это просто супер — {}",
        "Наверное, лучшее событие за день: {}",
        "Сейчас я счастлив, потому что {}",
        "У меня отличное настроение: {}"
    ],
    'грусть': [
        "Меня огорчило то, что {}",
        "Жаль, что {}",
        "Сердце разбилось, когда {}",
        "Сегодня чувствую грусть из-за того, что {}",
        "Немного грустно, потому что {}"
    ],
    'злость': [
        "Меня злит, что {}",
        "Я в бешенстве из-за {}",
        "Не могу поверить, как {}",
        "Нервничаю из-за того, что {}",
        "Это просто бесит — {}"
    ],
    'удивление': [
        "Ничего себе! {}",
        "Вот это да — {}",
        "Я в шоке: {}",
        "Неожиданно оказалось, что {}",
        "Кто бы мог подумать, {}"
    ]
}

extras = [
    "я получил подарок",
    "пошёл дождь неожиданно",
    "я встретил старого друга",
    "мне дали повышение",
    "результаты превзошли ожидания",
    "событие было удачным",
    "мне не хватило времени",
    "ошибки испортили планы",
    "это было сюрпризом",
    "это было весело",
    "все пошло не так",
    "я испытал сильные эмоции",
    "никогда не видел такого",
    "это было странно",
    "я доволен результатом"
]

N = 100_000
per_class = N // 4

with open('D:\emotions_100k.csv', 'w', encoding='utf-8', newline='') as f:
    writer = csv.writer(f)
    writer.writerow(['text', 'emotion'])
    for emotion, tmpl in templates.items():
        for _ in range(per_class):
            t = random.choice(tmpl)
            e = random.choice(extras)
            writer.writerow([t.format(e), emotion])

print("✅ Датасет создан: data/emotions_100k.csv")